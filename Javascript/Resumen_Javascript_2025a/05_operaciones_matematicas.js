/*
OPERACIONES MATEMÁTICAS BÁSICAS

+ --> suma
- --> resta
* --> multiplicacion
/ --> división
** --> eleva a un exponente o potencia
% --> módulo : el resto de la división

*/

console.log(20 % 6);

console.log(2 ** 3); // 2 elevado a 3 (2 al cubo)
console.log(25 ** (1/2)); // La raíz cuadrada de 25
