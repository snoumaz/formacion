/*
OPERADORES DE COMPARACIÓN
*/

// ==  Compara si dos valores son iguales (solo el valor, no el tipo)

console.log(1 == 1); // true

// Queremos comparar "1" es 1
console.log("1" == 1);

// === comparación estrica (Valor y tipo)
console.log("1" === 1);

// != Compara si dos cosas son diferentes

console.log(1 != 1); // false

// !== comparación estrica (Valor y tipo)
console.log("1" !== 1);

// > mayor que
console.log(5 > 4)
console.log(5 > 5);

// < menor que
console.log(5 < 4)
console.log(5 < 5);

// >= mayor o igual
// <= menor o igual
console.log(5 <= 4)
console.log(5 <= 5);
